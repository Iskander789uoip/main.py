from flask import Flask
import random

app = Flask(__name__)

# Список фактов о технологических зависимостях
facts_list = [
    "Люди в среднем проверяют свои смартфоны около 150 раз в день.",
    "Примерно 40% пользователей испытывают беспокойство, если оставили телефон дома.",
    "Более 80% подростков испытывают потребность ответить на сообщение в течение 5 минут.",
    "Феномен FOMO (страх упустить что-то важное) усиливается из-за социальных сетей.",
    "Алгоритмы стриминговых сервисов специально разработаны для поддержания зависимости от контента.",
]

# Главная страница с ссылкой на страницу факта
@app.route('/')
def home():
    return '''
        <h1>Добро пожаловать!</h1>
        <p>Хотите узнать интересный факт?</p>
        <a href="/random_fact">Посмотреть случайный факт!</a>
    '''

# Страница со случайным фактом
@app.route('/random_fact')
def random_fact():
    fact = random.choice(facts_list)  # Выбор случайного факта
    return f'<h1>Случайный факт о технологических зависимостях:</h1><p>{fact}</p>'

if __name__ == '__main__':
    app.run(debug=True)
